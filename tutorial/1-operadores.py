# Operadores aritméticos

print(2 + 3) # Suma
print(2 - 3) # Resta
print(2 * 3) # Multiplicación
print(2 / 3) # División
print(2 % 3) # Módulo
print(2 ** 3) # Exponente
print(2 // 3) # División entera

# Operadores de comparación

print(2 == 3) # a igual a b
print(2 != 3) # a diferente de b
print(2 > 3) # a mayor que b
print(2 < 3) # a menor que b
print(2 >= 3) # a mayor o igual que b
print(2 <= 3) # a menor o igual que b

